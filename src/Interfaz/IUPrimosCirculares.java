    private void jButtonStartActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonStartActionPerformed
        
        a = this.experto.crearHilos(this.jTextDesde.getText(), this.jTextHasta.getText());
        
        
        for (int i = 0; i < a.size(); i++) {
            Future<Integer> resultado = a.get(i);
            try {
                this.primosCirculares.append(resultado.get().toString() + ",");
                
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
        
        this.jTextTama単o.setText(String.valueOf(a.size()));
    }//GEN-LAST:event_jButtonStartActionPerformed

